    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        // TODO add your handling code here:
        String tag = txtTag.getText();
        Object selectedItem = cbFilter.getSelectedItem();
        try{
            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.setRowCount(0);
            ResultSet rs;
            switch(selectedItem.toString()){
                case "ID" -> rs = dbHelper.searchNotesRecordById(Integer.parseInt(tag));
                case "TAG" -> rs = dbHelper.searchNotesRecordByTag(tag);
                default -> rs = dbHelper.displayAllRecords();
            }
            rsAddRow(rs);
        } catch(SQLException ex){
            Logger.getLogger(DisplayNote.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchMouseClicked

