    public void initData() {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        try {
            ResultSet rs = dbHelper.displayAllRecords();
            while (rs.next()) {
                int id = rs.getInt("id");
                String title = rs.getString("title");
                String date = rs.getString("date");
                String tags = rs.getString("tags");
                String description = rs.getString("description");
                Object[] row = {id, title, date, tags, description};
                addRowToTable(row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DisplayNote.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void addRowToTable(Object[] dataRows) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.addRow(dataRows);
    }

